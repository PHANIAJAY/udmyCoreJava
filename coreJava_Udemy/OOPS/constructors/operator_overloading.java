package coreJava_Udemy.OOPS.constructors;

public class operator_overloading {
    public static void main(String[] args) {
        System.out.println(10+20);
        System.out.println("Ajay"+"SDE2");
        System.out.println("Ajay"+"SDE2"+25);
        System.out.println("Ajay"+"SDE2"+25+5);
        System.out.println("Ajay"+"SDE2"+(25+5));
    }

    //operator Overlaoding not supported by java whereas supported in only c++ .
    // In Java+ operator behaves based on paramaters .
    //if we use int--->peforms addition
    //if we use string--->performs concatination.

}
